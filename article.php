<script>
  // Smooth scroll and active link highlight
  const links = document.querySelectorAll('#author-nav a');
  const sections = [...links].map(link => document.querySelector(link.hash));

  function setActiveLink() {
    let index = sections.length - 1;
    while (index >= 0 && window.scrollY + 100 < sections[index].offsetTop) {
      index--;
    }
    links.forEach(link => link.classList.remove('active'));
    if (index >= 0) links[index].classList.add('active');
  }

  links.forEach(link =>
    link.addEventListener('click', e => {
      e.preventDefault();
      document.querySelector(link.hash).scrollIntoView({ behavior: 'smooth', block: 'start' });
    })
  );

  window.addEventListener('scroll', setActiveLink);
  setActiveLink();
</script>



