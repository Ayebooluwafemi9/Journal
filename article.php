            <h2>Guidelines for Authors</h2>

            <section id="submission-checklist">
            <h3>Submission Checklist</h3>
            <ul>
                <li>Manuscript categorized by paper typology.</li>
                <li>Title is concise and aligns with the journal’s mission.</li>
                <li>Author names and affiliations included (with one corresponding author).</li>
                <li>Abstract is standalone and does not exceed 250 words.</li>
                <li>Keywords provided (maximum of 6).</li>
                <li>Adheres to limits for text, tables, and figures.</li>
                <li>Includes essential sections: Title, Abstract, Introduction, Materials & Methods, Results, Discussion, References.</li>
                <li>Checked for grammar and plagiarism.</li>
                <li>Figures and tables are captioned and referenced in text.</li>
                <li>References cited in-text are listed in the References section.</li>
                <li>Permission granted for copyrighted content.</li>
                <li>Submitted via the official CUJPAS portal.</li>
            </ul>
            </section>

            <section id="types-of-articles">
            <h3>Types of Articles</h3>
            <p>Word limits exclude abstract and references:</p>
            <ul>
                <li>Original Research Articles – Max 5,000 words</li>
                <li>Review Articles – Max 5,000 words</li>
                <li>Case Studies – Max 5,000 words</li>
                <li>Reports – Max 2,500 words</li>
                <li>Letters – Max 2,000 words</li>
            </ul>
            </section>

            <section id="language">
            <h3>Language</h3>
            <p>Manuscripts must be in British or American English (not both).</p>
            </section>

            <section id="manuscript-organization">
            <h3>Manuscript Organization</h3>
            <p>Use Times New Roman, 10 pt, double-column, single-spaced, justified format in editable Word documents.</p>
            <ul>
                <li><strong>Title:</strong> Brief, max 20 words, no acronyms or formulas.</li>
                <li><strong>Authors:</strong>
                <ul>
                    <li>Full names and affiliations (with country)</li>
                    <li>Superscripted numbers for affiliations</li>
                    <li>Email for each author</li>
                    <li>Clearly marked corresponding author</li>
                </ul>
                </li>
                <li><strong>Abstract:</strong> Max 250 words, no acronyms.</li>
                <li><strong>Keywords:</strong> Max 6, avoid general terms and acronyms.</li>
                <li><strong>Introduction:</strong> Background, brief literature, study purpose.</li>
                <li><strong>Materials & Methods:</strong> Enough detail for reproducibility.</li>
                <li><strong>Results:</strong> Clear and concise findings.</li>
                <li><strong>Discussion:</strong> Interpret the significance of findings.</li>
                <li><strong>Conclusion:</strong> Synthesize key points.</li>
                <li><strong>Acknowledgements:</strong> If applicable.</li>
            </ul>
            </section>

            <section id="references">
            <h3>References</h3>
            <p>APA style (7th edition) is required. Include DOIs (e.g., https://doi.org/...)</p>
            </section>

            <section id="figures">
            <h3>Figures</h3>
            <ul>
                <li>Max of 8 figures</li>
                <li>Submit separately in TIFF format (not in-text)</li>
                <li>Caption each figure clearly</li>
                <li>Explain all symbols</li>
                <li>Minimum resolution: 300 DPI</li>
            </ul>
            </section>

            <section id="tables">
            <h3>Tables</h3>
            <ul>
                <li>Max of 3 tables</li>
                <li>No vertical or diagonal lines</li>
                <li>Column headings centered and capitalized</li>
                <li>Explain all abbreviations in footnotes</li>
            </ul>
            </section>

            <section id="abbreviations">
            <h3>Abbreviations</h3>
            <p>Define all abbreviations on first use.</p>
            </section>

            <section id="nomenclature-units">
            <h3>Nomenclature & Units</h3>
            <p>Use SI units; follow IUPAC nomenclature.</p>
            </section>

            <section id="math-equations">
            <h3>Mathematical Equations</h3>
            <p>Use standard notation; number equations consecutively.</p>
            </section>

